async def button_handler(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """Обработка нажатий на кнопки"""
    query = update.callback_query
    await query.answer()

    data = query.data

    if data == "main_menu":
        keyboard = [[InlineKeyboardButton("📚 Начать обучение", callback_data="back_to_topics")]]
        await query.edit_message_text(
            "🏠 **Главное меню**\n\nВыбери действие:",
            parse_mode='Markdown',
            reply_markup=InlineKeyboardMarkup(keyboard)
        )

    elif data == "back_to_topics":
        await query.edit_message_text(
            "📚 **Выбери тему:**\n\nВведи цифру темы или выбери из списка:",
            parse_mode='Markdown',
            reply_markup=get_topics_keyboard(0)
        )

    elif data.startswith("page_"):
        page = int(data.split("_")[1])
        await query.edit_message_text(
            "📚 **Выбери тему:**",
            parse_mode='Markdown',
            reply_markup=get_topics_keyboard(page)
        )

    elif data == "theory_mode":
        await query.edit_message_text(
            "📚 **Режим теории**\n\nВведи **цифру темы** (например: 5), чтобы получить формулы и теоремы.\n\n"
            "Или выбери тему из списка:",
            parse_mode='Markdown',
            reply_markup=get_topics_keyboard(0)
        )

    elif data == "test_mode":
        await query.edit_message_text(
            "✏️ **Режим проверки знаний**\n\nВведи **цифру темы** (например: 7), и я дам тебе задание.\n\n"
            "Реши пример и отправь ответ!",
            parse_mode='Markdown',
            reply_markup=get_topics_keyboard(0)
        )

    elif data.startswith("topic_"):
        topic_num = data.split("_")[1]
        topic = MATH_TOPICS.get(topic_num)
        if topic:
            await query.edit_message_text(
                f"*{topic['номер']}. {topic['название']}*\n\n"
                f"📐 Предмет: {topic['предмет']}\n\n"
                f"Что хочешь сделать?",
                parse_mode='Markdown',
                reply_markup=get_topic_keyboard(topic_num)
            )

    elif data.startswith("show_"):
        topic_num = data.split("_")[1]
        topic = MATH_TOPICS.get(topic_num)
        if topic:
            response = f"*{topic['номер']}. {topic['название']}*\n\n"
            response += f"*Формулы:*\n"
            for formula in topic['формулы']:
                response += f"• `{formula}`\n"
            response += f"\n*Теоремы:*\n"
            for theorem in topic['теоремы']:
                response += f"• {theorem}\n"

            await query.edit_message_text(
                response,
                parse_mode='Markdown',
                reply_markup=get_topic_keyboard(topic_num)
            )

    elif data.startswith("test_"):
        topic_num = data.split("_")[1]
        topic = MATH_TOPICS.get(topic_num)
        if topic and topic.get('примеры'):
            example = random.choice(topic['примеры'])
            # Сохраняем информацию о тесте
            user_tests[update.effective_user.id] = {
                'topic': topic_num,
                'question': example['вопрос'],
                'answer': example['ответ']
            }

            await query.edit_message_text(
                f"*Тест по теме:* {topic['название']}\n\n"
                f"❓ *Вопрос:* {example['вопрос']}\n\n"
                f"Отправь ответ в чат!",
                parse_mode='Markdown',
                reply_markup=get_topic_keyboard(topic_num)
            )

async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """Обработка текстовых сообщений"""
    user_id = update.effective_user.id
    text = update.message.text.strip()

    # Проверяем, не число ли это (выбор темы)
    if text.isdigit():
        topic_num = text
        topic = MATH_TOPICS.get(topic_num)
        if topic:
            await update.message.reply_text(
                f"*{topic['номер']}. {topic['название']}*\n\n"
                f"📐 Предмет: {topic['предмет']}\n\n"
                f"Что хочешь сделать?",
                parse_mode='Markdown',
                reply_markup=get_topic_keyboard(topic_num)
            )
            return

    # Проверяем, не ответ ли это на тест
    if user_id in user_tests:
        test_data = user_tests[user_id]
        user_answer = text.strip().lower()
        correct_answer = test_data['answer'].lower()

        # Простое сравнение (можно усложнить)
        if user_answer == correct_answer or user_answer.replace(' ', '') == correct_answer.replace(' ', ''):
            result = "✅ **Правильно!** Молодец!"
        else:
            result = f"❌ **Неправильно.** Правильный ответ: {test_data['answer']}"

        # Удаляем тест
        del user_tests[user_id]

        # Возвращаем к теме
        topic = MATH_TOPICS.get(test_data['topic'])
        await update.message.reply_text(
            f"{result}\n\nМожешь продолжить изучение:",
            reply_markup=get_topic_keyboard(test_data['topic'])
        )
        return

    # Если ничего не подошло
    await update.message.reply_text(
        "Я не понял команду. Используй кнопки или введи цифру темы (1-20).",
        reply_markup=get_topics_keyboard(0)
    )

