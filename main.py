def get_topics_keyboard(page=0):
    """Создает клавиатуру со списком тем"""
    topics_list = list(MATH_TOPICS.values())
    # По 5 тем на странице
    start_idx = page * 5
    end_idx = start_idx + 5
    current_topics = topics_list[start_idx:end_idx]

    keyboard = []
    for topic in current_topics:
        emoji = "🔢" if topic["предмет"] == "Алгебра" else "📐"
        button_text = f"{emoji} {topic['номер']}. {topic['название']}"
        keyboard.append([InlineKeyboardButton(button_text, callback_data=f"topic_{topic['номер']}")])

    # Кнопки навигации
    nav_buttons = []
    if page > 0:
        nav_buttons.append(InlineKeyboardButton("◀️ Назад", callback_data=f"page_{page-1}"))
    if end_idx < len(topics_list):
        nav_buttons.append(InlineKeyboardButton("Вперед ▶️", callback_data=f"page_{page+1}"))

    if nav_buttons:
        keyboard.append(nav_buttons)

    # Кнопки режимов
    keyboard.append([InlineKeyboardButton("📚 Теория (по цифре)", callback_data="theory_mode")])
    keyboard.append([InlineKeyboardButton("✏️ Проверка знаний", callback_data="test_mode")])
    keyboard.append([InlineKeyboardButton("🏠 Главное меню", callback_data="main_menu")])

    return InlineKeyboardMarkup(keyboard)

def get_topic_keyboard(topic_num):
    """Клавиатура для конкретной темы"""
    keyboard = [
        [InlineKeyboardButton("📚 Формулы и теоремы", callback_data=f"show_{topic_num}")],
        [InlineKeyboardButton("✏️ Проверить знания", callback_data=f"test_{topic_num}")],
        [InlineKeyboardButton("◀️ К списку тем", callback_data="back_to_topics")],
        [InlineKeyboardButton("🏠 Главное меню", callback_data="main_menu")]
    ]
    return InlineKeyboardMarkup(keyboard)

